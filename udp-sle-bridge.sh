#!/bin/sh
while [ -h "$PRG" ]; do
  ls=`ls -ld "$PRG"`
  link=`expr "$ls" : '.*-> \(.*\)$'`
  if expr "$link" : '/.*' > /dev/null; then
    PRG="$link"
  else
    PRG=`dirname "$PRG"`/"$link"
  fi
done

# Get standard environment variables
PRGDIR=`dirname "$PRG"`

CLASSPATH="$PRGDIR/lib/*"

export CLASSPATH
exec java org.yamcs.sle.udpslebridge.SleUdpBridge "$@"
